			  <br>
			 
				<h2>Konstruktionsvollholz (KVH)</h2>
				<br>
					<p>Konstruktionsvollholz (KVH) ist ein veredeltes Bauschnittholz. Es weist, im Gegensatz zum natürlichen 
					Vollholz, eine DIN-Normen entsprechende Qualität auf. Dadurch ist Konstruktionsvollholz (KVH) verzugsärmer als Vollholz. Die Anforderungen, die vom Konstruktionsvollholz
					erfüllt werden, umfassen die folgenden Punkte:</p>
					
						<ul>
 							<li> Holzfeuchte: Konstruktionsvollholz(KVH) weist durch technische Trocknung einen definierten Feuchtigkeitsgrad von 15 &plusmn; 3% auf. </li>
 				 			<li> Einschnittart: Durch "herzgetrennten" Einschnitt wird die Bildung von Schwindrissen verringert.</li>
 							<li> Oberflächenbeschaffenheit: gehobelt und gefast (KVH sichtbar) bzw. egalisiert und gefast (KVH nicht sichtbar).</li>
 				 
						</ul>
						
						<p>Mehr Informationen zum Thema Konstruktionsvollholz (KVH) finden Sie z.B. auf <a href="https://de.wikipedia.org/wiki/Konstruktionsvollholz" target="blank"><span class=red>Wikipedia.</span></a>
						</p>
						
					
					<br>
					<br>
				<h3>Brettschichtholz (BSH) / Leimbinder</h3>
				<br>
					<p>
						Brettschichtholz (BSH) und Leimbinder sind synonym verwendete Begriffe. 
						Leimbinder bestehen aus mehrfach verleimten Brettschichtlamellen. Dadurch 
						bieten sie sich immer dann an, wenn es auf Maßhaltigkeit, Verzugsärme, Rissfreiheit und eine ansprechende
						Optik ankommt.
						<br>
						<br>
						Mehr Informationen zum Thema Brettschichtholz (BSH) / Leimbinder finden sie z.B. auf <a href="https://de.wikipedia.org/wiki/Brettschichtholz" target="blank"><span class=red>Wikipedia.</span></a>
					</p>
	
			
