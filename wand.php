				<h2>Fassadenelemente aus WPC (Wood Polymer Composite)</h2>
				<br>
				<p>Fassaden aus <b>WPC</b> sind der neue Stern am "Fassadenhimmel". 
				Fassadenelemente aus WPC bestehen zu ca. 70% aus Holzfasern verbunden mit einem Polymer-Material.
				Diese Kombination bieten für Sie vielfältige Vorteile: Kein Splittern, kein Verzug, praktisch keine 
				Längenausdehnung und Farbveränderung. Noch dazu sind Fassaden aus WPC leicht zu verarbeiten und pflegeleicht.
				
				Mehr Informationen zum Thema WPC finden Sie z.B. auf 
				<a href="https://de.wikipedia.org/wiki/Wood-Plastic-Composite" target="blank"><span class=red>Wikipedia</span></a>.
