			 <p><b>Parkett</b> ist ein beliebter Fußboden aus Vollholz. Das Vollholz - in der Regel robustes Hartholz - ist in Parkettböden 
			 in kleine Stücke geteilt, die in bestimmten Mustern angeordnet sind. Dadurch ergibt sich ein für Parkett typisches, eher fleinformatiges
			 Muster. Dieses Muster unterscheidet Parketböden von den Böden aus Massivholzdielen, die eine eher großformatige Struktur aufweisen.
			 
			<br>
			<br>
			Unser Sortiment umfasst Parkett aus folgenden Holzarten:
			 
			 </p>
